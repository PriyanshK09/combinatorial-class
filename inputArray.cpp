#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    int arr[n];
    // Input of Array
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    // Output of Array
    for (int i = 0; i < n; i++)
    {
        cout << arr[i] << " ";
    }
    return 0;
}